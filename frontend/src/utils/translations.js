// دالة للحصول على الترجمة
export const t = (key, fallback = '') => {
  const keys = key.split('.');
  let value = translations;
  
  for (const k of keys) {
    if (value && typeof value === 'object' && k in value) {
      value = value[k];
    } else {
      return fallback || key;
    }
  }
  
  return value || fallback || key;
};

export default translations;
